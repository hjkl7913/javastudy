package ch01;

public class OperEx01 {

	public static void main(String[] args) {
		int n1, n2;
		n1 = 5;
		n2 = 3;

		// ctrl+s (컴파일) -> shift+F10 (실행)
		// ctrl+alt+L (정렬)
		System.out.println(n1 + n2);
		System.out.println(n1 - n2);
		System.out.println(n1 / n2);
		System.out.println(n1 * n2);
		System.out.println(n1 % n2);
	}

}
